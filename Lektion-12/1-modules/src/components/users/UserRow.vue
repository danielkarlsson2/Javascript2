<template>
    <tr :class="{inactive: !user.active}">
      <th scope="row">{{ index + 1 }}</th>
      <td>{{ user.name }}</td>
      <td>{{ user.active ? 'Active' : 'Inactive' }}</td>
      <td class="text-end">
          <button @click="toggleActive(user)" class="btn btn-sm btn-info me-2 btn-fixed-width">{{ user.active ? 'Inactive' : 'Activate' }}</button>
          <button @click="removeUser(user.id)" class="btn btn-sm btn-info btn-fixed-width"><i class="fa-solid fa-trash"></i></button>
      </td>
    </tr>
</template>

<script>
import { mapActions } from 'vuex'
export default {
    props: ['user', 'index'],
    methods: {
        ...mapActions(['removeUser', 'toggleActive'])
    }
}
</script>

<style>
.btn-fixed-width {
    width: 100px;
}
.inactive{
    color: #dcdcdc;
}
</style>